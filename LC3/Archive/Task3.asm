.ORIG x3000
; R0 is input for sbr
; R3 and R4 is output for sbr

; prompt user
LEA R0 PROMPT
PUTS

; get user input
IN

; sub 48 from R0 so 0 corresponds to 0
LD R1 CHAR_OFFSET
ADD R1 R0 R1 ; store the char in R1

; check if its 0
ADD R3 R1 #0
BRnp BR0
LEA R0 MONDAY
PUTS
HALT
BR0

; check if its 1
ADD R3 R1 #-1
BRnp BR1
LEA R0 TUESDAY
PUTS
HALT
BR1

; check if its 2
ADD R3 R1 #-2
BRnp BR2
LEA R0 WEDNESDAY
PUTS
HALT
BR2

; check if its 3
ADD R3 R1 #-3
BRnp BR3
LEA R0 THURSDAY
PUTS
HALT
BR3

; check if its 4
ADD R3 R1 #-4
BRnp BR4
LEA R0 FRIDAY
PUTS
HALT
BR4

; check if its 5
ADD R3 R1 #-5
BRnp BR5
LEA R0 SATURDAY
PUTS
HALT
BR5

; check if its 6
ADD R3 R1 #-6
BRnp BR6
LEA R0 SUNDAY
PUTS
HALT
BR6

HALT

CHAR_OFFSET .FILL #-48
PROMPT      .STRINGZ "Please enter number: "
MONDAY      .STRINGZ "MONDAY"
TUESDAY     .STRINGZ "TUESDAY"
WEDNESDAY   .STRINGZ "WEDNESDAY"
THURSDAY    .STRINGZ "THURSDAY"
FRIDAY      .STRINGZ "FRIDAY"
SATURDAY    .STRINGZ "SATURDAY"
SUNDAY      .STRINGZ "SUNDAY"

.END